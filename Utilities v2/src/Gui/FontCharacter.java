package Gui;

public class FontCharacter {
	
	private final int x, y, width;
	
	public FontCharacter(int x, int y, int width){
		this.x = x;
		this.y = y;
		this.width = width;
	}
	
	public int getX(){
		return x;
	}
	
	public int getY(){
		return y;
	}
	
	public int getWidth(){
		return width;
	}
	
}
